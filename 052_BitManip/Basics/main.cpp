#include <iostream>

int main() {
    int a = 10;
    int b = 6;
    int c = a & b;
    std::cout<<a<<" & "<<b<<" : "<<c<<std::endl;

    int d = a | b;
    std::cout<<a<<" | "<<b<<" : "<<d<<std::endl;

    int e = a ^ b;
    std::cout<<a<<" ^ "<<b<<" : "<<e<<std::endl;

    unsigned int f = ~e;
    std::cout<<"~"<<e<<" : "<<f<<std::endl;

    int g = e << d;
    std::cout<<e<<" << "<<d<<" : "<<g<<std::endl;

    int h = g >> e;
    std::cout<<g<<" >> "<<e<<" : "<<h<<std::endl;
}

